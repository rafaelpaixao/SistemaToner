    public ImpressoraCadastro(Sistema sistema) {
        this.sistema = sistema;
        initComponents();

        try {
            this.listaSetores = this.sistema.getListaDeSetores();
            this.listaModelos = this.sistema.getListaDeModelosImpressoras();
            if (!listaSetores.isEmpty() && !listaModelos.isEmpty()) {
                
                String[] setores = new String[this.listaSetores.size()];
                for (int i = 0; i < this.listaSetores.size(); i++) {
                    setores[i] = listaSetores.get(i).getNome() + " (" + listaSetores.get(i).getEmpresa() + ")";
                }
                
                String[] modelos = new String[this.listaModelos.size()];
                for (int i = 0; i < this.listaModelos.size(); i++) {
                    modelos[i] = listaModelos.get(i).getModeloImpressora();
                }
                
                DefaultComboBoxModel modelS = new DefaultComboBoxModel(setores);
                this.jComboBoxSetor.setModel(modelS);
                
                DefaultComboBoxModel modelM = new DefaultComboBoxModel(modelos);
                this.jComboBoxModelo.setModel(modelM);
            }
        } catch (SQLException ex) {
            Alertas.erroBanco(this,ex.toString());
        }
}

