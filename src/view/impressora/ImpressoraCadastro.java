    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean sucesso = false;
        
        try{
            Impressora x = new Impressora();
            x.setIdSetor(listaSetores.get(this.jComboBoxSetor.getSelectedIndex()).getId());
            x.setIdModeloImpressora(listaModelos.get(this.jComboBoxModelo.getSelectedIndex()).getId());
            x.setTipo(this.jComboBoxTipo.getSelectedItem().toString());
            x.setCodigo(this.jTextCodigo.getText());
            try {
                sucesso = this.sistema.cadastrarImpressora(x);
            } catch (SQLException ex) {
                Alertas.erroBanco(this,ex.toString());
            }
            
        }catch (NumberFormatException e){
            Alertas.erroFormatoEntrada(this);
        }
        Alertas.sucessoOuErro(this,sucesso);
        if(sucesso) this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        if(listaSetores.isEmpty()){
            Alertas.mensagem(this, "É preciso cadastrar um setor!");
            this.dispose();
        }else if(listaModelos.isEmpty()){
            Alertas.mensagem(this, "É preciso cadastrar um modelo de impressora!");
            this.dispose();
        }
    }//GEN-LAST:event_formComponentShown

